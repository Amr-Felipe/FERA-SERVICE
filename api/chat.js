export default async function handler(req, res) {
  try {
    const { message } = req.body;

    console.log("KEY:", process.env.GEMINI_KEY);

    const response = await fetch(
      `https://generativelanguage.googleapis.com/v1/models/gemini-1.5-flash:generateContent?key=${process.env.GEMINI_KEY}`,
      {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          contents: [
            {
              parts: [{ text: message }],
            },
          ],
        }),
      }
    );

    const data = await response.json();

    console.log("GEMINI RESPONSE:", data);

    res.status(200).json({
      reply: data.candidates?.[0]?.content?.parts?.[0]?.text || "Sem resposta"
    });

  } catch (err) {
    console.error("ERRO REAL:", err);
    res.status(500).json({ error: err.message });
  }
}
